import { eachDayOfInterval } from "date-fns";

export async function getCabin(id) {
  const { data, error } = await supabase
    .from("cabins")
    .select("*")
    .eq("id", id)
    .single();

  if (error) {
    console.log("ERROR:  ", error);
    console.error(error);
    notFound();
  }

  console.log("CABIN: ", data);
  return data;
}

